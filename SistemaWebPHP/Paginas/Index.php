Exercício 1: Busca Binária

Descrição:
Implemente um programa que realiza uma busca binária em uma lista ordenada de números inteiros.

Teste de Sistema:
*Cenário 1: Número Encontrado*

Entrada:

  Lista: [1, 3, 5, 7, 9, 11, 13, 15]
  Buscar: 7

Saída Esperada:

  Resultado: O número 7 foi encontrado na posição 3.


*Cenário 2: Número Não Encontrado*

Entrada:

  Lista: [2, 4, 6, 8, 10]
  Buscar: 3

Saída Esperada:

  Resultado: O número 3 não foi encontrado na lista.


Exercício 2: Ordenação de Lista

Descrição:
Crie um programa que ordena uma lista de números inteiros em ordem crescente.

Teste de Sistema:
*Cenário 1: Lista Desordenada*

Entrada:

  Lista: [9, 2, 5, 1, 7, 3]

Saída Esperada:

  Resultado: Lista ordenada: [1, 2, 3, 5, 7, 9]


*Cenário 2: Lista Já Ordenada*

Entrada:

  Lista: [1, 2, 3, 4, 5]

Saída Esperada:

  Resultado: Lista já está ordenada: [1, 2, 3, 4, 5]


Exercício 3: Calculadora de Fatorial

Descrição:
Escreva um programa que calcula o fatorial de um número fornecido pelo usuário.

Teste de Sistema:
*Cenário 1: Fatorial de 5*

Entrada:

  Calcular o fatorial de 5

Saída Esperada:

  Resultado: O fatorial de 5 é 120.


*Cenário 2: Fatorial de 0*

Entrada:

  Calcular o fatorial de 0

Saída Esperada:

  Resultado: O fatorial de 0 é 1.


Exercício 4: Validador de Senha

Descrição:
Crie um programa que verifica se uma senha atende aos seguintes critérios:
  Pelo menos 8 caracteres de comprimento.
  Contém pelo menos uma letra maiúscula e uma letra minúscula.
  Pelo menos um número.

Teste de Sistema:
*Cenário 1: Senha Válida*

Entrada:

  Senha: Abcdefg1

Saída Esperada:

  Resultado: A senha é válida.


*Cenário 2: Senha Inválida (Falta de Número)*

Entrada:

  Senha: Abcdefgh

Saída Esperada:

  Resultado: A senha deve conter pelo menos um número.


Exercício 5: Jogo da Forca

Descrição:
Implemente o jogo da forca em que o computador escolhe uma palavra aleatória e o jogador tenta adivinhar as letras.

Teste de Sistema:
*Cenário 1: Palavra Adivinhada Corretamente*

Entrada:

  Palavra escolhida pelo computador: "elefante"
  Letras escolhidas pelo jogador: e, l, f, a, n, t

Saída Esperada:

  Resultado: Parabéns, você adivinhou a palavra!


*Cenário 2: Tentativas Esgotadas*

Entrada:

  Palavra escolhida pelo computador: "banana"
  Letras escolhidas pelo jogador: x, y, z, m, n, b

Saída Esperada:

  Resultado: Você esgotou suas tentativas. A palavra era "banana".


Exercício 6: Simulador de Banco

Descrição:
Crie um programa que simule as operações de um banco, como depósito, saque e verificação de saldo.

Teste de Sistema:
*Cenário 1: Depósito e Saque Bem Sucedidos*

Entrada:

  Saldo inicial: R$ 500
  Depósito: R$ 200
  Saque: R$ 100

Saída Esperada:

  Resultado: Saldo atual: R$ 600


*Cenário 2: Tentativa de Saque com Saldo Insuficiente*

Entrada:

  Saldo inicial: R$ 100
  Saque: R$ 200

Saída Esperada:

  Resultado: Saldo insuficiente para realizar o saque.


